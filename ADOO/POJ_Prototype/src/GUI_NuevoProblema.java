    public void metodo(String tipo) throws IOException
    {
            //Creamos selector de apertura
      JFileChooser fc=new JFileChooser();
 
        //Abrimos la ventana, guardamos la opcion seleccionada por el usuario
        int seleccion = fc.showOpenDialog(null);

        //Si el usuario, pincha en aceptar
        if(seleccion==JFileChooser.APPROVE_OPTION) {
            //Seleccionamos el fichero
            fichero=fc.getSelectedFile();
            
            System.out.println(fichero.getAbsolutePath());
            switch(tipo.toString())
            {
                case "fuente.pdf":
                    file2 = new File("C:\\Users\\Carlo\\Downloads\\ProblemasPDF_Programacion\\"+txt_nombre.getText()+tipo);
                    break;
                case "input.txt":
                    inp = new File("C:\\Users\\Carlo\\Downloads\\ProblemasPDF_Programacion\\"+txt_nombre.getText()+tipo);
                    break;
                case "output.txt":
                    out = new File("C:\\Users\\Carlo\\Downloads\\ProblemasPDF_Programacion\\"+txt_nombre.getText()+tipo);
                    break;
            }
            
            
            //compileFile(fichero.getAbsolutePath());
            
            btn_fuente_archivo.setText(fichero.getAbsolutePath());
        }
    }
