    private void btn_subirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_subirActionPerformed
        try {
            switch(contador)
            {
                case 0:
                    Servicio.copyFileUsingStream(fichero, file2);
                    p1.setRutaArchivo(file2.getAbsolutePath());
                   
                    break;
                case 1:
                     Servicio.copyFileUsingStream(fichero, inp);
                      p1.setRuta_input(inp.getAbsolutePath());
                      
                    break;
                case 2:
                    Servicio.copyFileUsingStream(fichero, out);
                     
                    p1.setRuta_output(out.getAbsolutePath());
                    p1.setId_problem_setter(id_problemsetter);
                    p1.setNombre(txt_nombre.getText());
                    
                    RegistroProblema rg1 = new RegistroProblema();
                    rg1.registrar(p1);
                    break;
            }
          
            
            contador = (contador + 1)%3;
        } catch (IOException ex) {
            Logger.getLogger(GUI_NuevoProblema.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GUI_NuevoProblema.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUI_NuevoProblema.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_subirActionPerformed

